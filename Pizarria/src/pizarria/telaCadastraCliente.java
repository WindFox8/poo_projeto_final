    private void tabClientesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabClientesMouseClicked
        //Pega a linha clicada
        linhaClicadaParaAtualizacao = this.tabClientes.rowAtPoint(evt.getPoint());
        //Pega o contato da linha clicada
        Cliente c = this.TabelaModelClientes.getCliente(linhaClicadaParaAtualizacao);
        //Seta os dados no formulário
        this.clienteSelecionadoParaAtualizacao = c;
        txtNome.setText(c.getNome());
        txtSobreNome.setText(c.getSobreNome());
        txtTel.setText(c.getTelefone());
    }//GEN-LAST:event_tabClientesMouseClicked

    private void txtExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtExcluirActionPerformed
        //botao para exlcuir o usuário selecionado
        Cliente c = this.clienteSelecionadoParaAtualizacao;
        String id = c.getId() + "";
        
        
        
        this.TabelaModelClientes.removeCliente(c);
        System.out.println(c.getNome() + " / " + c.getId());
        BancoDadosClientes.listaClientes.remove(id,c);
        clienteSelecionadoParaAtualizacao = null;
        linhaClicadaParaAtualizacao=-1;
        txtNome.setText("");
        txtSobreNome.setText("");
        txtTel.setText("");
    }//GEN-LAST:event_txtExcluirActionPerformed

